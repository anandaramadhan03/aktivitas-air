let currentQuestion = 0;
let score = 0;

const benarSound = new Audio("audio/benar.mp3");
const salahSound = new Audio("audio/salah.mp3");

function loadQuestion() {
  const q = questions[currentQuestion];
  document.getElementById("question").textContent = q.question;
  document.getElementById("question-image").src = q.image;
  document.getElementById("question-audio").src = q.audio;
  document.getElementById("question-audio").style.display = "block";

  const options = document.getElementById("options");
  options.innerHTML = "";

  q.options.forEach((option, index) => {
    const btn = document.createElement("button");
    btn.className = "option";
    btn.textContent = option;
    btn.onclick = () => checkAnswer(index);
    options.appendChild(btn);
  });
}

function checkAnswer(selected) {
  const feedback = document.getElementById("feedback");
  const isCorrect = selected === questions[currentQuestion].answer;

  if (isCorrect) {
    score++;
    feedback.textContent = "✅ Benar!";
    feedback.style.color = "green";
    benarSound.play();
  } else {
    feedback.textContent = "❌ Salah!";
    feedback.style.color = "red";
    salahSound.play();
  }

  setTimeout(() => {
    feedback.textContent = "";
    currentQuestion++;
    if (currentQuestion < questions.length) {
      loadQuestion();
    } else {
      showScore();
    }
  }, 1500);
}

function showScore() {
  document.getElementById("question").style.display = "none";
  document.getElementById("question-image").style.display = "none";
  document.getElementById("question-audio").style.display = "none";
  document.getElementById("options").style.display = "none";
  document.getElementById("next-button").style.display = "none";
  const scoreText = `Nilai kamu: ${score} dari ${questions.length}`;
  document.getElementById("score").textContent = scoreText;
  document.getElementById("score").style.display = "block";
}

document.getElementById("next-button").addEventListener("click", loadQuestion);
loadQuestion();
